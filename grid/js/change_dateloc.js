function changeDateloc1(event) {
    var newDate = prompt("Please enter your new trip date in format DD MM YY:", " ");
    if (newDate != null) {
        var newLocation = prompt("Please enter your new trip location:", " ");
        document.getElementById("dateloc-button-1").style.display = "none";
        document.getElementById("new-dateloc-1").innerHTML = newDate + " " + newLocation;
    } else if ((newDate == null) || (newLocation == null)) {
        alert("Please enter the new date AND location!");
        document.getElementById("new-dateloc-1").innerHTML = "Notime and nowhere trip";
    }
}

